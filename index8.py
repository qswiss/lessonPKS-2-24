import random

def ugadai_chislo():
    zagadannoe = random.randint(1, 100)
    popitki = 0

    print("Я загадал число от 1 до 100. Попробуй угадать!")

    while True:
        try:
            vvod = int(input("Твоё число: "))
            popitki += 1

            if vvod < zagadannoe:
                print("Число должно быть выше.")
            elif vvod > zagadannoe:
                print("Число должно быть ниже.")
            else:
                print(f"Молодец! Ты угадал число за {popitki} попыток.")
                break
        except ValueError:
            print("Пожалуйста, введи целое число.")

