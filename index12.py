# Задача: Транспортная компания ведёт учёт грузов, которые нужно доставить.
# Каждый груз имеет: название груза, вес (в кг), город назначения, стоимость доставки (в сомах), статус - "в пути" или "доставлено"
# 1) Показывает все грузы.
# 2) Добавляет новый груз.
# 3) Находит все грузы, направленные в указанный город.
# 4) Находит самый тяжёлый груз.
# 5) Показывает все грузы дешевле заданной стоимости.
# 6) Меняет статус груза на "доставлено".
# 7) Увеличивает стоимость всех доставок на 10%.
# 0) Завершает работу программы.
cargo_list = [
    {"название": "Телевизор", "вес": 15, "город": "Ош", "стоимость": 1200, "статус": "в пути"},
    {"название": "Холодильник", "вес": 45, "город": "Бишкек", "стоимость": 3000, "статус": "в пути"},
    {"название": "Книги", "вес": 5, "город": "Талас", "стоимость": 500, "статус": "Доставлен"},]
while True:
    print("""
          1 — Показать все грузы
          2 — Добавить новый груз
          3 — Найти все грузы в указанном городе
          4 — Найти самый тяжёлый груз
          5 — Показать грузы дешевле заданной стоимости
          6 — Изменить статус груза на 'Доставлен
          7 — Увеличить стоимость всех доставок на 10%
          0 — Завершить работу""")
    choice = input("Выберите действие: ")

    if choice == "1":
        print("\nВсе грузы:")
        for cargo in cargo_list:
            print(f"{cargo['название']} — {cargo['вес']} кг, {cargo['город']}, {cargo['стоимость']} сом, статус: {cargo['статус']}")

    elif choice == "2":
        name = input("Введите название груза: ").strip()
        weight = input("Введите вес (в кг): ").strip()
        city = input("Введите город назначения: ").strip()
        cost = input("Введите стоимость доставки (в сомах): ").strip()
        status = "в пути"
        if not weight.isdigit() or not cost.isdigit():
            print("Ошибка: вес и стоимость должны быть числами.")
            continue
        cargo_list.append({
            "название": name,
            "вес": int(weight),
            "город": city,
            "стоимость": int(cost),
            "статус": status
        })
        print(f"Груз '{name}' добавлен.")
    elif choice == "3":
        city = input("Введите город для поиска: ").strip()
        found = [cargo for cargo in cargo_list if cargo["город"].lower() == city.lower()]
        if found:
            print(f"\nГрузы, направленные в {city}:")
            for cargo in found:
                print(f"{cargo['название']} — {cargo['вес']} кг, {cargo['стоимость']} сом, статус: {cargo['статус']}")
        else:
            print("Нет грузов для этого города.")
    elif choice == "4":
        heaviest = max(cargo_list, key=lambda x: x["вес"])
        print(f"\nСамый тяжёлый груз: {heaviest['название']} — {heaviest['вес']} кг, город: {heaviest['город']}.")
    elif choice == "5":
        limit = input("Введите максимальную стоимость: ").strip()
        if not limit.isdigit():
            print("Ошибка: стоимость должна быть числом.")
            continue
        limit = int(limit)
        cheaper = [cargo for cargo in cargo_list if cargo["стоимость"] < limit]
        if cheaper:
            print(f"\nГрузы дешевле {limit} сом:")
            for cargo in cheaper:
                print(f"{cargo['название']} — {cargo['стоимость']} сом, город: {cargo['город']}")
        else:
            print("Нет грузов дешевле заданной стоимости.")
    elif choice == "6":
        name = input("Введите название груза для изменения статуса: ").strip()
        found = False
        for cargo in cargo_list:
            if cargo["название"].lower() == name.lower():
                cargo["статус"] = "Доставлен"
                print(f"Статус груза '{cargo['название']}' изменён на 'Доставлен'.")
                found = True
                break
        if not found:
            print("Груз не найден.")
    elif choice == "7":
        for cargo in cargo_list:
            old_cost = cargo["стоимость"]
            cargo["стоимость"] = round(cargo["стоимость"] * 1.1)
        print("Стоимость всех доставок увеличена на 10%.")
    elif choice == "0":
        print("Программа завершена.")
        break
    else:
        print("Неверный выбор. Попробуйте снова.")